// IF || ELSE || ELSE IF ------------------------------------------------------

// check if a number is even or odd.
let num = 12
if(num % 2 == 0) {
    console.log("the number is even.");
}

else {
    console.log("The number is odd.");
}

// check if the given number is more than, less than or equal to 10.
let num2 = 90
if(num2 > 10) {
    console.log("the number is bigger than 10");
}
else if(num2 < 10) {
    console.log("the number is smaller than 10");
}
else if(num2 == 10) {
    console.log("the number is equal to 10");
}
else {
    console.log("the number is incorect");
}
